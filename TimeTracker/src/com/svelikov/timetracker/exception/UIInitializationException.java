package com.svelikov.timetracker.exception;

/**
 * @author Factor5
 * 
 */
public class UIInitializationException extends Exception {
	public UIInitializationException() {
	}

	public UIInitializationException(String arg0) {
		super(arg0);
	}

	public UIInitializationException(Throwable arg0) {
		super(arg0);
	}

	public UIInitializationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
