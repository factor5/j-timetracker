package com.sve.timetracker.exception;

/**
 * @author Svilen Velikov
 */
public class UIInitializationException extends Exception {
	public UIInitializationException() {
	}

	public UIInitializationException(final String arg0) {
		super(arg0);
	}

	public UIInitializationException(final Throwable arg0) {
		super(arg0);
	}

	public UIInitializationException(final String arg0, final Throwable arg1) {
		super(arg0, arg1);
	}

}
